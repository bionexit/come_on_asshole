// 简单的哈希函数，用于确定性选择
function getHashIndex(original: string, options: string[], seed: string = 'default_seed'): number {
  let hash = 0;
  const str = seed + '|' + original;
  for (let i = 0; i < str.length; i++) {
    const char = str.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash; // Convert to 32bit integer
  }
  return Math.abs(hash) % options.length;
}

/**
 * 对姓名进行脱敏处理
 * @param name 真实姓名（2-4个汉字，支持复姓）
 * @param seed 确定性种子
 * @returns 脱敏后的姓名（同音字替换）
 */
export function maskName(name: string, seed: string = 'zhangsan_project_2024'): string {
  if (!name || name.length < 2) {
    return name;
  }

  const maskedChars: string[] = [];
  let i = 0;

  while (i < name.length) {
    const char = name[i];

    // 检查复姓（只在开头检查）
    if (i === 0 && name.length >= 2) {
      const doubleSurname = name.substring(i, i + 2);
      if (surnameMap[doubleSurname]) {
        const options = surnameMap[doubleSurname];
        const replacement = options[getHashIndex(doubleSurname, options, seed)];
        maskedChars.push(replacement);
        i += 2;
        continue;
      }
    }

    // 单姓处理
    if (i === 0 && surnameMap[char]) {
      const options = surnameMap[char];
      const replacement = options[getHashIndex(char, options, seed)];
      maskedChars.push(replacement);
    } else if (nameCharMap[char]) {
      // 名字用字处理
      const options = nameCharMap[char];
      const replacement = options[getHashIndex(char, options, seed)];
      maskedChars.push(replacement);
    } else {
      // 未收录的字保持原样
      maskedChars.push(char);
    }

    i++;
  }

  return maskedChars.join('');
}
